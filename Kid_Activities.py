def run(*kids):
    for name in kids:
        print(f"{name} ran like a fool!")

run("Pam", "Sam", "Andrea", "Will")


def swing(*kids):
    for name in kids:
        print(f"{name} was swinging like a fool!")

swing("Marybeth", "Ariel", "Kevin", "Courtney")


def slide(*kids):
    for name in kids:
        print(f"{name} was sliding like a fool!")

slide("Mike", "Jack", "Jennifer", "Earl")

def hide(*kids):
    for name in kids:
        print(f"{name} was hiding like a fool!")

hide("Henry", "Heather", "Hayley", "Hugh")